class printnumber
  {
  void add()
   {
     
    int row=1;
     while(row<=4)
     {
       int col=1;
       while(col<=4)
        {
         if(row+col==5 || row==col)
         {
         System.out.print(0);
          
        }
        else
       {

       System.out.print(1);
         }
         col++;
        }
         System.out.println();
         row++;
        }
     }
   
   public static void main(String args[])
     {
       printnumber inba=new printnumber();
          inba.add();
       }
}
